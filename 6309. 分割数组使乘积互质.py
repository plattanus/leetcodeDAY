from typing import List

import math

class Solution:
    def findValidSplit(self, nums: List[int]) -> int:
        if len(nums) == 1:
            return -1
        r = 0
        for i in range(len(nums)):
            l = nums[i]
            for j in range(len(nums)-1, r, -1):
                g = math.gcd(l, nums[j])
                if g != 1:
                    if j == len(nums)-1:
                        return -1
                    r = j
                    break
            if i == r:
                break
        return r
        

